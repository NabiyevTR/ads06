package ru.geekbrains.ntr_ads06;

import java.util.Random;

public class Test6 {
    private static final int NUMBER_OF_TREES = 20;
    private static final int MAX_HEIGHT = 4;
    private static final int MAX_NUMBER_OF_ELEMENTS = (int) Math.pow(2, MAX_HEIGHT)-1 ;
    private static final int MIN_VALUE = -100;
    private static final int MAX_VALUE = 100;

    public static void main(String[] args) {

        int balancedTreeCounter = 0;

        for (int i = 0; i < NUMBER_OF_TREES; i++) {
            Tree<Integer> tree = new TreeImpl<>(MAX_HEIGHT);
            for (int j = 0; j < MAX_NUMBER_OF_ELEMENTS; j++) {
                tree.add(getRandom(MIN_VALUE, MAX_VALUE));
            }
            balancedTreeCounter = tree.isBalanced() ? ++balancedTreeCounter : balancedTreeCounter;
            System.out.printf("Tree № %d is%sbalanced.\n", i + 1, tree.isBalanced() ? " " : " not ");
           tree.display();
        }
        System.out.printf("Balanced tree %%: %d", (int) (100.0f * balancedTreeCounter / NUMBER_OF_TREES));
    }

    public static int getRandom(int min, int max) {
        Random random = new Random();
        return random.nextInt(max - min + 1) + min;
    }
}

